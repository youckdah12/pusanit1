package fileupload;

public class FileuploadBean {
		private int num;
		private String upfile;
		private int size;
		
		public int getNum() {
			return num;
		}
		public void setNum(int num) {
			this.num = num;
		}
		public String getUpfile() {
			return upfile;
		}
		public void setUpfile(String upfile) {
			this.upfile = upfile;
		}
		public int getSize() {
			return size;
		}
		public void setSize(int size) {
			this.size = size;
		}
}
